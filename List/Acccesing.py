#Accessing List Items

thislist = ["apple","banana","cherry" , "orange","kiwi","melon","mango"]

print(thislist[1])


#Negative Indexing

print(thislist[-1])

#Range of indexes

print(thislist[2:5])

#By leaving start value , the range will start at the first item :

print(thislist[:4])

#By leaving end value , the range will go on to the end of the list :

print(thislist[1:])

#Negative Range Indexes

print(thislist[-4:-1])





