#Escape Characters

#Single quote or double quotes
txt = "We are the so-called \"Viking\" from the north "
print(txt)

#Backslash
txt1 = "This will insert one \\ (Bakslash)"
print(txt1)

#New Line
txt2 = "Hello \ World!"
print(txt2)

#Carriage Return
txt3 = "Hello\r World!"
print(txt3)

#Tab
txt4 = "Hello \t World!"
print(txt4)


