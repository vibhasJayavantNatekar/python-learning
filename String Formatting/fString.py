#Placeholders and Modifiers

price = 49

txt = f"The price is {price} dollars"

print(txt)

#Display the price with two decimals

txtDec = f"The price is {price:.2f} dollars"

print(txtDec)

#Perform Operations in F - String

txtop = f"The price is {20 * 59} dollars"

print(txtop)


