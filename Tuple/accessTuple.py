#Print the second item in the tuple

thistuple = ("apple" , "banana" , "cherry" , "orange" , "kiwi" , "melon" , "mango")
print(thistuple)

print(thistuple[1])

#Negative indexing
# -1 refer to the last item

print(thistuple[-1])

#Range of indexing

#Return the third , fourth and fifth item

print(thistuple[2:5])

#This is return the items from the beginning  but not include "Kiwi"

print(thistuple[:4])

# This returns the item from "cherry" and to the end.

print(thistuple[2:])

#this include -4 but exclude -1

print(thistuple[-4:-1])







